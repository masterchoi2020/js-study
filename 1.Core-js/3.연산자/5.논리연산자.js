
var t = true, f = false;

// AND 연산
console.log(t && t);
console.log(t && f);
console.log(f && t);
console.log(f && f);

// OR 연산
console.log("=================================");

console.log(t || t);
console.log(t || f);
console.log(f || t);
console.log(f || f);

// NOT 연산
console.log("=================================");

console.log(!true);
console.log(!false);